#Function that returns the largest number of two numbers 

def largest_number(num_1, num_2):
     """ 
     Find the largest number out of the two integers
     
     param num_1: first number
     type num_1: int
     param num_2: second number
     type num_2: int
     return: largest number
     """
     return max(num_1, num_2) 